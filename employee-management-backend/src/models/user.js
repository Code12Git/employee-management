const mongoose = require('mongoose')
const { Schema } = mongoose;

const authModel = new Schema({
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ['admin', 'employee']
    }
}, { timestamps: true })

module.exports = mongoose.model('Auth', authModel)